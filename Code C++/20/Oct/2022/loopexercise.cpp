#include<iostream>
using namespace std; 

int main(){
  for(int i=0; i<11; i++){
    cout<<i<<" ";
  }
  cout<<endl;
  for(int i=4; i<14; i++){
    cout<<i<<" ";
  }
  cout<<endl;
  for(int i=1; i<16; i+=2){
    cout<<i<<" ";
  }
  cout<<endl;
  for(int i=0; i<15; i+=2){
    cout<<i<<" ";
  }
  cout<<endl;
  for(int i=0; i<13; i+=2){
    cout<<i/2<<" ";
  }
  cout<<endl;
  for(int i=0; i<28; i+=3){
    cout<<i<<" ";
  }
  cout<<endl;
}